(DIETs)</span></p>

<p class=MsoNormal style='margin-left:46.5pt;text-indent:-.25in;line-height:
20.4pt;background:white'><span lang=EN-IN style='font-size:10.0pt;font-family:
Symbol;color:#464646'>·<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span><span
lang=EN-IN style='font-size:15.5pt;font-family:"Open Sans",sans-serif;
color:#464646'>Strengthening of Secondary Teachers Education Institutions into
Colleges of Teacher Education (CTEs) and Institutes of Advanced Study in
Education (IASEs)</span></p>

<p class=MsoNormal style='margin-left:46.5pt;text-indent:-.25in;line-height:
20.4pt;background:white'><span lang=EN-IN style='font-size:10.0pt;font-family:
Symbol;color:#464646'>·<span style='font:7.0pt "Times New Roman"'>&nbsp; </span></span><span
lang=EN-IN style='font-size:15.5pt;font-family:"Open Sans",sans-serif;
color:#464646'>Strengthening of State Councils of Educational Research and
Training (SCERTs)</span></p>

<p class=MsoNormal style='margin:7.5pt;text-align:justify;line-height:20.4pt;
background:white'><span lang=EN-IN style='font-size:15.5pt;font-family:"Open Sans",sans-serif;
color:#464646'>Under the Scheme, recurring and non-recurring Central assistance
is provided to the State Governments as resource support to the DIETs, CTEs,
IASEs and SCERTs</span></p>

<p class=MsoNormal style='margin-top:15.0pt;margin-right:0in;margin-bottom:
7.5pt;margin-left:0in;line-height:normal;background:white'><span lang=EN-IN
