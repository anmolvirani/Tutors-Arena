training of elementary &amp; secondary school teachers and for provision of
academic resource support to elementary and secondary schools. The Scheme had,
the following component:</span></p>

<p class=MsoNormal style='margin-left:46.5pt;text-indent:-.25in;line-height:
20.4pt;background:white'><span lang=EN-IN style='font-size:10.0pt;font-family:
